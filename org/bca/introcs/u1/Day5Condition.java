package org.bca.introcs.u1;

public class Day5Condition {

	public static void main(String[] args) {
		
		char ch = 'b';
		//single quote is character and double quote is string
		
		int i = (ch == 'a') ? 8 : 2;
		//this is a conditional statement, where this the format: (condition) ? if true this is the value : if false this is the value;
		//the condition should give a true or false value
		System.out.println(i);

	}

}
